/** 旧版开场白片段，用于迁移本地草稿 */
const LEGACY_OPENING_SNIPPETS = [
  '您好，我是您的财务助手',
  '您好，我是您的 AI 财务助手',
  '接下来需要您用文字耐心说明',
  '暂不支持上传图片',
  '请用文字录入财务情况'
]

function isLegacyOpeningMessage(content) {
  const t = String(content || '')
  if (!t) return false
  if (t === FIRST_ASSISTANT_MESSAGE) return false
  return LEGACY_OPENING_SNIPPETS.some((s) => t.includes(s))
}

